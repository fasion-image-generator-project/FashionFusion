/**
 * 메인 ImageGeneratepage 컴포넌트
 * 전체 애플리케이션의 상태와 로직을 관리
 */
const ImageGeneratepage = () => {
  // 상태 관리
  const [styleGanParams, setStyleGanParams] = useState({
    truncation: 0.7,    // 기본값 0.7, 범위: 0.1 ~ 1.0
    noise: 0.5,         // 기본값 0.5, 범위: 0.0 ~ 1.0
    strength: 0.8      // 기본값 0.8, 범위: 0.1 ~ 1.0
  });

  const [state, setState] = useState({
    prompt: "",              // 텍스트 프롬프트
    initialImage: "",        // 초기 생성된 이미지
    finalImage: "",          // 변환된 이미지
    loading: false,          // 로딩 상태
    error: "",              // 에러 메시지
    selectedInitialModel: "Stable Diffusion 1.5",  // 초기 이미지 생성용 모델
    selectedFinalModel: "Disco GAN"     // 최종 이미지 생성용 모델
  });

  // 히스토리 상태 - localStorage와 동기화
  const [history, setHistory] = useState(() => {
    const savedHistory = localStorage.getItem('imageGenerationHistory');
    return savedHistory ? JSON.parse(savedHistory) : [];
  });

  // 사이드바 표시 상태
  const [showSidebar, setShowSidebar] = useState(false);

  // Refs
  const imageSectionRef = useRef(null);
  const sidebarTimeoutRef = useRef(null);

  // 메모이제이션된 값들
  const isGenerateDisabled = useMemo(() =>
    state.loading || !state.prompt,
    [state.loading, state.prompt]
  );

  const canShowModelSelect = useMemo(() =>
    Boolean(state.initialImage),
    [state.initialImage]
  );

  // 다크모드 상태 관리
  const [isDarkMode, setIsDarkMode] = useState(() => {
    const savedTheme = localStorage.getItem('theme');
    return savedTheme ? savedTheme === 'dark' : window.matchMedia('(prefers-color-scheme: dark)').matches;
  });

  // 다크모드 변경 시 localStorage 저장
  useEffect(() => {
    localStorage.setItem('theme', isDarkMode ? 'dark' : 'light');
  }, [isDarkMode]);

  // 효과
  useEffect(() => {
    // 히스토리 변경 시 localStorage에 저장
    localStorage.setItem('imageGenerationHistory', JSON.stringify(history));
  }, [history]);

  // 커스텀 프리셋 상태 관리
  const [customPresets, setCustomPresets] = useState(() => {
    const saved = localStorage.getItem('styleGanCustomPresets');
    return saved ? JSON.parse(saved) : {};
  });

  // 현재 선택된 프리셋 상태
  const [activePreset, setActivePreset] = useState(null);

  // 프리셋 적용 함수
  const handlePresetClick = useCallback((presetName, presetValues) => {
    setStyleGanParams(presetValues);
    setActivePreset(presetName);
  }, []);

  // 현재 설정을 새 프리셋으로 저장
  const handleSavePreset = useCallback(() => {
    const presetName = prompt('Enter a name for this preset:');
    if (presetName) {
      const newPresets = {
        ...customPresets,
        [presetName]: { ...styleGanParams }
      };
      setCustomPresets(newPresets);
      localStorage.setItem('styleGanCustomPresets', JSON.stringify(newPresets));
      setActivePreset(presetName);
    }
  }, [customPresets, styleGanParams]);

  // 파라미터 변경 시 activePreset 초기화
  const handleStyleGanParamChange = useCallback((param, value) => {
    setStyleGanParams(prev => ({
      ...prev,
      [param]: value
    }));
    setActivePreset(null);  // 파라미터가 변경되면 현재 선택된 프리셋 초기화
  }, []);

  // 콜백 함수들
  /**
   * 히스토리 항목 삭제 처리
   */
  const handleDeleteHistory = useCallback((id) => {
    setHistory(prev => prev.filter(item => item.id !== id));
  }, []);

  /**
   * 히스토리 전체 삭제 처리
   */
  const handleClearHistory = useCallback(() => {
    if (window.confirm('모든 히스토리를 삭제하시겠습니까?')) {
      setHistory([]);
    }
  }, []);

  /**
   * 히스토리 항목 복원 처리
   */
  const handleRestoreHistory = useCallback((item) => {
    setState(prev => ({
      ...prev,
      prompt: item.prompt,
      initialImage: item.initialImage,
      finalImage: item.finalImage,
      selectedInitialModel: item.model,
      selectedFinalModel: item.model
    }));

    // Style GAN-ada 파라미터 복원
    if (item.parameters) {
      setStyleGanParams(item.parameters);
    }
  }, []);

  /**
   * 히스토리 내보내기 처리
   */
  const handleExportHistory = useCallback(() => {
    const historyData = JSON.stringify(history, null, 2);
    const blob = new Blob([historyData], { type: 'application/json' });
    const url = window.URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `fashion-fusion-history-${new Date().toISOString().split('T')[0]}.json`;
    document.body.appendChild(a);
    a.click();
    window.URL.revokeObjectURL(url);
    document.body.removeChild(a);
  }, [history]);

  /**
   * 히스토리 가져오기 처리
   */
  const handleImportHistory = useCallback((event) => {
    const file = event.target.files[0];
    if (file) {
      const reader = new FileReader();
      reader.onload = (e) => {
        try {
          const importedHistory = JSON.parse(e.target.result);
          if (Array.isArray(importedHistory)) {
            if (window.confirm('기존 히스토리에 추가하시겠습니까? "취소"를 선택하면 기존 히스토리를 대체합니다.')) {
              setHistory(prev => [...prev, ...importedHistory]);
            } else {
              setHistory(importedHistory);
            }
          }
        } catch (err) {
          alert('잘못된 형식의 파일입니다.');
        }
      };
      reader.readAsText(file);
    }
    event.target.value = '';
  }, []);

  /**
   * 초기 이미지 생성 처리
   */
  const handleInitialGeneration = useCallback(async (seed) => {
    if (!state.prompt || !state.selectedInitialModel) return;

    setState(prev => ({ ...prev, loading: true, error: "" }));

    try {
      if (USE_DUMMY_DATA) {
        await new Promise(resolve => setTimeout(resolve, 1000));
        setState(prev => ({
          ...prev,
          initialImage: DUMMY_IMAGES[state.selectedInitialModel].INITIAL,
          finalImage: ""
        }));
      } else {
        const imageData = await generateInitialImage(state.prompt, state.selectedInitialModel, seed);
        // base64 데이터를 data URL 형식으로 변환
        const imageUrl = `data:image/png;base64,${imageData}`;
        setState(prev => ({
          ...prev,
          initialImage: imageUrl,
          finalImage: ""
        }));
      }
    } catch (err) {
      setState(prev => ({ ...prev, error: err.message }));
    } finally {
      setState(prev => ({ ...prev, loading: false }));
    }
  }, [state.prompt, state.selectedInitialModel]);

  /**
   * 최종 이미지 변환 처리
   */
  const handleFinalGeneration = useCallback(async (seed) => {
    if (!state.initialImage) return;

    setState(prev => ({ ...prev, loading: true, error: "" }));

    try {
      if (USE_DUMMY_DATA) {
        await new Promise(resolve => setTimeout(resolve, 1000));
        setState(prev => ({
          ...prev,
          finalImage: DUMMY_IMAGES[state.selectedFinalModel].FINAL
        }));
      } else {
        const imageData = await generateFinalImage(state.initialImage, state.selectedFinalModel, seed);
        setState(prev => ({
          ...prev,
          finalImage: `data:image/png;base64,${imageData}`
        }));
      }
    } catch (err) {
      setState(prev => ({ ...prev, error: err.message }));
    } finally {
      setState(prev => ({ ...prev, loading: false }));
    }
  }, [state.initialImage, state.selectedFinalModel]);

  /**
   * 이미지 재생성 처리
   */
  const handleRegenerate = useCallback(() => {
    if (state.finalImage) {
      // 랜덤 시드 생성 (0-1000000 사이의 정수)
      const seed = Math.floor(Math.random() * 1000000);
      handleFinalGeneration(seed);
    } else {
      // 랜덤 시드 생성 (0-1000000 사이의 정수)
      const seed = Math.floor(Math.random() * 1000000);
      handleInitialGeneration(seed);
    }
  }, [state.finalImage, handleFinalGeneration, handleInitialGeneration]);

  /**
   * 모든 입력 초기화 처리
   */
  const handleReset = useCallback(() => {
    setState(prev => ({
      ...prev,
      prompt: "",
      initialImage: "",
      finalImage: "",
      error: ""
    }));
    setStyleGanParams({
      truncation: 0.7,
      noise: 0.5,
      strength: 0.8
    });
  }, []);

  /**
   * 모델 변경 처리
   */
  const handleInitialModelChange = useCallback((model) => {
    setState(prev => ({ ...prev, selectedInitialModel: model }));
  }, []);

  const handleFinalModelChange = useCallback((model) => {
    setState(prev => ({ ...prev, selectedFinalModel: model }));
  }, []);

  // 렌더링 메서드들
  /**
   * 프롬프트 표시 영역 렌더링
   */
  const renderPromptDisplay = useMemo(() => (
    state.prompt && (
      <PromptDisplay>
        <div style={{ display: "flex", alignItems: "center", gap: "10px" }}>
          <span style={{ fontWeight: "bold" }}>prompt:</span>
          <span>{state.prompt}</span>
        </div>
      </PromptDisplay>
    )
  ), [state.prompt]);

  /**
   * 히스토리 항목들 렌더링
   */
  const renderHistoryItems = useMemo(() => (
    history.map(item => (
      <HistoryItemComponent
        key={item.id}
        item={item}
        onRestore={handleRestoreHistory}
        onDelete={handleDeleteHistory}
      />
    ))
  ), [history, handleRestoreHistory, handleDeleteHistory]);

  return (
    <ThemeProvider theme={isDarkMode ? darkTheme : lightTheme}>
      <GlobalStyle />
      <Container>
        <ThemeToggle
          onClick={() => setIsDarkMode(!isDarkMode)}
          aria-label={isDarkMode ? "Switch to light mode" : "Switch to dark mode"}
        >
          {isDarkMode ? '🌞' : '🌙'}
        </ThemeToggle>
        <MainContent>
          {renderPromptDisplay}
          <ContentArea>
            <ImageControlArea>
              {/* Initial Model Selection and Prompt Input - Only visible when no images exist */}
              {!state.initialImage && !state.finalImage && (
                <>
                  <ModelSelect>
                    <ModelOptions>
                      <ModelSection>
                        <SectionTitle>Select Initial Model</SectionTitle>
                        <div style={{
                          display: "grid",
                          gridTemplateColumns: "repeat(auto-fit, minmax(300px, 1fr))",
                          gap: "16px"
                        }}>
                          {INITIAL_MODEL_OPTIONS.map((option) => (
                            <ModelCard
                              key={option.id}
                              isSelected={state.selectedInitialModel === option.id}
                              onClick={() => handleInitialModelChange(option.id)}
                            >
                              <ModelHeader>
                                <ModelIcon>
                                  {option.id.includes("Stable Diffusion") ? "🎨" :
                                    option.id.includes("Disco") ? "💃" : "✨"}
                                </ModelIcon>
                                <ModelTitle>{option.label}</ModelTitle>
                              </ModelHeader>
                              <ModelDescription>
                                {MODEL_DESCRIPTIONS[option.id].description}
                              </ModelDescription>
                              <ModelFeatures>
                                <FeatureItem isPro>
                                  <FeatureIcon>✓</FeatureIcon>
                                  {MODEL_DESCRIPTIONS[option.id].pros[0]}
                                </FeatureItem>
                                <FeatureItem isPro={false}>
                                  <FeatureIcon>✗</FeatureIcon>
                                  {MODEL_DESCRIPTIONS[option.id].cons[0]}
                                </FeatureItem>
                              </ModelFeatures>
                            </ModelCard>
                          ))}
                        </div>
                      </ModelSection>
                    </ModelOptions>
                  </ModelSelect>

                  <Form
                    onSubmit={(e) => {
                      e.preventDefault();
                      // 랜덤 시드 생성 (0-1000000 사이의 정수)
                      const seed = Math.floor(Math.random() * 1000000);
                      handleInitialGeneration(seed);
                    }}
                    aria-label="Image generation form"
                  >
                    <PromptInputContainer>
                      <PromptLabel htmlFor="prompt-input">Describe your fashion item</PromptLabel>
                      <PromptInputWrapper>
                        <PromptInput
                          id="prompt-input"
                          type="text"
                          value={state.prompt}
                          onChange={(e) => setState(prev => ({ ...prev, prompt: e.target.value }))}
                          placeholder="Enter a detailed description of the fashion item you want to generate..."
                          disabled={state.loading}
                          aria-required="true"
                          maxLength={200}
                          hasError={state.error}
                        />
                        <PromptCounter count={state.prompt.length} maxLength={200}>
                          {state.prompt.length}/200
                        </PromptCounter>
                      </PromptInputWrapper>
                      <PromptHint>
                        💡 Tip: Be specific about colors, materials, and style details
                      </PromptHint>
                      {state.error && (
                        <PromptError>
                          ⚠️ {state.error}
                        </PromptError>
                      )}
                      <PromptExamples>
                        {PROMPT_EXAMPLES.map((example, index) => (
                          <ExampleButton
                            key={index}
                            onClick={() => setState(prev => ({ ...prev, prompt: example }))}
                            type="button"
                          >
                            {example}
                          </ExampleButton>
                        ))}
                      </PromptExamples>
                    </PromptInputContainer>
                    <Button
                      type="submit"
                      disabled={isGenerateDisabled || !state.selectedInitialModel}
                      aria-label="Generate initial image"
                      style={{ marginTop: '16px' }}
                    >
                      Generate Image
                    </Button>
                  </Form>
                </>
              )}

              {/* Final Model Selection - Visible after initial image generation */}
              {state.initialImage && (
                <>
                  <ModelSelect>
                    <ModelOptions>
                      <ModelSection>
                        <SectionTitle>Select Final Model</SectionTitle>
                        <div style={{
                          display: "grid",
                          gridTemplateColumns: "repeat(auto-fit, minmax(300px, 1fr))",
                          gap: "16px"
                        }}>
                          {FINAL_MODEL_OPTIONS.map((option) => (
                            <ModelCard
                              key={option.id}
                              isSelected={state.selectedFinalModel === option.id}
                              onClick={() => handleFinalModelChange(option.id)}
                            >
                              <ModelHeader>
                                <ModelIcon>
                                  {option.id.includes("Stable Diffusion") ? "🎨" :
                                    option.id.includes("Disco") ? "💃" : "✨"}
                                </ModelIcon>
                                <ModelTitle>{option.label}</ModelTitle>
                              </ModelHeader>
                              <ModelDescription>
                                {MODEL_DESCRIPTIONS[option.id].description}
                              </ModelDescription>
                              <ModelFeatures>
                                <FeatureItem isPro>
                                  <FeatureIcon>✓</FeatureIcon>
                                  {MODEL_DESCRIPTIONS[option.id].pros[0]}
                                </FeatureItem>
                                <FeatureItem isPro={false}>
                                  <FeatureIcon>✗</FeatureIcon>
                                  {MODEL_DESCRIPTIONS[option.id].cons[0]}
                                </FeatureItem>
                              </ModelFeatures>
                            </ModelCard>
                          ))}
                        </div>
                      </ModelSection>
                    </ModelOptions>
                  </ModelSelect>

                  {/* Style GAN-ada Parameters */}
                  <SliderPanel show={state.selectedFinalModel === "Style GAN-ada"}>
                    {/* Presets */}
                    <PresetContainer>
                      {Object.entries(STYLE_GAN_PRESETS).map(([name, values]) => (
                        <PresetButton
                          key={name}
                          active={activePreset === name}
                          onClick={() => handlePresetClick(name, values)}
                        >
                          {name}
                        </PresetButton>
                      ))}
                      {Object.entries(customPresets).map(([name, values]) => (
                        <PresetButton
                          key={name}
                          active={activePreset === name}
                          onClick={() => handlePresetClick(name, values)}
                        >
                          {name}
                        </PresetButton>
                      ))}
                      <SavePresetButton onClick={handleSavePreset}>
                        Save Current
                      </SavePresetButton>
                    </PresetContainer>

                    <SliderContainer>
                      <SliderHeader>
                        <div style={{ display: 'flex', alignItems: 'center' }}>
                          <SliderLabel>Truncation (Style Variation)</SliderLabel>
                          <Tooltip text={PARAMETER_DESCRIPTIONS.truncation} />
                        </div>
                        <SliderValue>{styleGanParams.truncation.toFixed(2)}</SliderValue>
                      </SliderHeader>
                      <StyledSlider
                        min="0.1"
                        max="1.0"
                        step="0.01"
                        value={styleGanParams.truncation}
                        onChange={(e) => handleStyleGanParamChange('truncation', parseFloat(e.target.value))}
                        aria-label="Style variation control"
                      />
                    </SliderContainer>

                    <SliderContainer>
                      <SliderHeader>
                        <div style={{ display: 'flex', alignItems: 'center' }}>
                          <SliderLabel>Noise (Detail Level)</SliderLabel>
                          <Tooltip text={PARAMETER_DESCRIPTIONS.noise} />
                        </div>
                        <SliderValue>{styleGanParams.noise.toFixed(2)}</SliderValue>
                      </SliderHeader>
                      <StyledSlider
                        min="0.0"
                        max="1.0"
                        step="0.01"
                        value={styleGanParams.noise}
                        onChange={(e) => handleStyleGanParamChange('noise', parseFloat(e.target.value))}
                        aria-label="Noise level control"
                      />
                    </SliderContainer>

                    <SliderContainer>
                      <SliderHeader>
                        <div style={{ display: 'flex', alignItems: 'center' }}>
                          <SliderLabel>Strength (Transform Intensity)</SliderLabel>
                          <Tooltip text={PARAMETER_DESCRIPTIONS.strength} />
                        </div>
                        <SliderValue>{styleGanParams.strength.toFixed(2)}</SliderValue>
                      </SliderHeader>
                      <StyledSlider
                        min="0.1"
                        max="1.0"
                        step="0.01"
                        value={styleGanParams.strength}
                        onChange={(e) => handleStyleGanParamChange('strength', parseFloat(e.target.value))}
                        aria-label="Transform strength control"
                      />
                    </SliderContainer>
                  </SliderPanel>
                </>
              )}

              {/* Image display logic */}
              {state.initialImage && !state.finalImage && (
                <ImageContainer>
                  {state.loading && (
                    <LoadingOverlay>
                      <SkeletonContainer>
                        <SkeletonImage />
                        <SkeletonText />
                        <SkeletonText />
                        <SkeletonButton />
                      </SkeletonContainer>
                    </LoadingOverlay>
                  )}
                  <MemoizedImageMagnifier
                    imageUrl={state.initialImage}
                    alt="Initial"
                  />
                </ImageContainer>
              )}

              {state.loading && !state.initialImage && (
                <ImageContainer>
                  <SkeletonContainer>
                    <SkeletonImage />
                    <SkeletonText />
                    <SkeletonText />
                    <SkeletonButton />
                  </SkeletonContainer>
                </ImageContainer>
              )}

              {state.finalImage && (
                <ImageContainer>
                  {state.loading && (
                    <LoadingOverlay>
                      <SkeletonContainer>
                        <SkeletonImage />
                        <SkeletonText />
                        <SkeletonText />
                        <SkeletonButton />
                      </SkeletonContainer>
                    </LoadingOverlay>
                  )}
                  <MemoizedImageMagnifier
                    imageUrl={state.finalImage}
                    alt="Transformed"
                  />
                </ImageContainer>
              )}

              {/* Button controls */}
              {state.initialImage && (
                <ButtonGroup role="group" aria-label="Image control buttons">
                  <Button
                    onClick={handleRegenerate}
                    disabled={state.loading}
                    aria-label="Regenerate image"
                  >
                    regenerate
                  </Button>
                  <Button
                    onClick={handleReset}
                    disabled={state.loading}
                    aria-label="Reset all inputs"
                  >
                    reset
                  </Button>
                  {!state.finalImage ? (
                    <Button
                      onClick={() => {
                        // 랜덤 시드 생성 (0-1000000 사이의 정수)
                        const seed = Math.floor(Math.random() * 1000000);
                        handleFinalGeneration(seed);
                      }}
                      disabled={state.loading || !state.selectedFinalModel}
                      aria-label="Transform image style"
                    >
                      style transform
                    </Button>
                  ) : (
                    <Button
                      variant="success"
                      onClick={() => {
                        const byteCharacters = atob(state.finalImage.split(',')[1]);
                        const byteNumbers = new Array(byteCharacters.length);
                        for (let i = 0; i < byteCharacters.length; i++) {
                          byteNumbers[i] = byteCharacters.charCodeAt(i);
                        }
                        const byteArray = new Uint8Array(byteNumbers);
                        const blob = new Blob([byteArray], { type: 'image/png' });
                        const url = window.URL.createObjectURL(blob);
                        const a = document.createElement('a');
                        a.href = url;
                        a.download = 'transformed_image.png';
                        document.body.appendChild(a);
                        a.click();
                        window.URL.revokeObjectURL(url);
                        document.body.removeChild(a);
                      }}
                      aria-label="Download transformed image"
                    >
                      download
                    </Button>
                  )}
                </ButtonGroup>
              )}
            </ImageControlArea>
          </ContentArea>

          {state.error && (
            <ErrorMessage>
              <p>{state.error}</p>
            </ErrorMessage>
          )}
        </MainContent>

        <HistoryButton showSidebar={showSidebar}>
          <Button
            onClick={() => setShowSidebar(!showSidebar)}
            aria-expanded={showSidebar}
            aria-controls="history-sidebar"
            aria-label={showSidebar ? "Hide history sidebar" : "Show history sidebar"}
          >
            {showSidebar ? "Hide History" : "Show History"}
          </Button>
        </HistoryButton>

        <SidebarTrigger
          onMouseEnter={() => {
            if (sidebarTimeoutRef.current) {
              clearTimeout(sidebarTimeoutRef.current);
            }
            setShowSidebar(true);
          }}
        />

        <Sidebar
          show={showSidebar}
          onMouseLeave={() => {
            sidebarTimeoutRef.current = setTimeout(() => {
              setShowSidebar(false);
            }, 300);
          }}
          role="complementary"
          aria-label="Generation history"
          id="history-sidebar"
        >
          <SidebarHeader>
            <h3 style={{ margin: 0 }}>Generation History</h3>
            <div style={{ display: "flex", gap: "8px" }}>
              {history.length > 0 && (
                <>
                  <Button
                    variant="success"
                    onClick={handleExportHistory}
                    aria-label="Export history"
                  >
                    Export
                  </Button>
                  <Button
                    variant="danger"
                    onClick={handleClearHistory}
                    aria-label="Clear all history"
                  >
                    Clear All
                  </Button>
                </>
              )}
              <input
                type="file"
                accept=".json"
                onChange={handleImportHistory}
                style={{ display: "none" }}
                id="history-import"
                aria-label="Import history from file"
              />
              <Button
                onClick={() => document.getElementById('history-import').click()}
                aria-label="Import history"
              >
                Import
              </Button>
            </div>
          </SidebarHeader>

          <HistoryList role="list" aria-label="History items">
            {renderHistoryItems}
            {history.length === 0 && (
              <div style={{
                textAlign: "center",
                color: "#666",
                padding: "20px"
              }}>
                No history yet
              </div>
            )}
          </HistoryList>
        </Sidebar>
      </Container>
    </ThemeProvider>
  );
};

export default ImageGeneratepage;
