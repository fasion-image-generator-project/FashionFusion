/**
 * 이미지 확대 컴포넌트
 * 마우스 오버 시 이미지의 특정 부분을 확대하여 보여줌
 */
const ImageMagnifier = ({ imageUrl, alt }) => {
  const [position, setPosition] = useState({ x: 0, y: 0 });
  const [showMagnifier, setShowMagnifier] = useState(false);
  const imgRef = useRef(null);

  const magnifierWidth = 200;
  const magnifierHeight = 200;
  const zoomLevel = 2.5;

  const handleMouseMove = (e) => {
    const elem = imgRef.current;
    if (!elem) return;

    const { top, left, width, height } = elem.getBoundingClientRect();
    const x = e.clientX - left;
    const y = e.clientY - top;

    setPosition({
      x: Math.max(0, Math.min(x, width)),
      y: Math.max(0, Math.min(y, height))
    });
  };

  return (
    <div
      style={{
        position: "relative",
        width: "100%",
        height: "400px",
        display: "flex",
        justifyContent: "center",
        alignItems: "center"
      }}
    >
      <img
        ref={imgRef}
        src={imageUrl}
        alt={alt}
        style={{
          maxWidth: "100%",
          height: "400px",
          objectFit: "contain",
          borderRadius: "4px"
        }}
        onMouseEnter={() => setShowMagnifier(true)}
        onMouseLeave={() => setShowMagnifier(false)}
        onMouseMove={handleMouseMove}
      />
      {showMagnifier && (
        <div
          style={{
            position: "absolute",
            left: `${position.x - magnifierWidth / 2}px`,
            top: `${position.y - magnifierHeight / 2}px`,
            width: `${magnifierWidth}px`,
            height: `${magnifierHeight}px`,
            border: "2px solid #007bff",
            borderRadius: "4px",
            pointerEvents: "none",
            overflow: "hidden",
            backgroundColor: "white",
            zIndex: 100,
            boxShadow: "0 2px 8px rgba(0, 0, 0, 0.2)"
          }}
        >
          <img
            src={imageUrl}
            alt={alt}
            style={{
              position: "absolute",
              left: `${-position.x * (zoomLevel - 1) - (magnifierWidth / 2)}px`,
              top: `${-position.y * (zoomLevel - 1) - (magnifierHeight / 2)}px`,
              maxWidth: "none",
              height: `${400 * zoomLevel}px`,
              objectFit: "contain"
            }}
          />
        </div>
      )}
    </div>
  );
};

